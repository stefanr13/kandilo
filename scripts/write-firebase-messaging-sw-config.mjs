#!/usr/bin/env node

import { existsSync, mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { config as loadDotenv } from 'dotenv';

const envFiles = ['.env.local', '.env'];
for (const envFile of envFiles) {
  const path = resolve(envFile);
  if (existsSync(path)) {
    loadDotenv({ path, override: false });
  }
}

const outputPath = resolve('public/firebase-messaging-sw-config.js');
const requiredEnv = {
  apiKey: 'VITE_FIREBASE_API_KEY',
  authDomain: 'VITE_FIREBASE_AUTH_DOMAIN',
  projectId: 'VITE_FIREBASE_PROJECT_ID',
  storageBucket: 'VITE_FIREBASE_STORAGE_BUCKET',
  messagingSenderId: 'VITE_FIREBASE_MESSAGING_SENDER_ID',
  appId: 'VITE_FIREBASE_APP_ID',
};

const firebaseConfig = Object.fromEntries(
  Object.entries(requiredEnv).map(([key, envName]) => [key, process.env[envName]?.trim() ?? ''])
);
const missing = Object.entries(requiredEnv)
  .filter(([key]) => !firebaseConfig[key])
  .map(([, envName]) => envName);

mkdirSync(dirname(outputPath), { recursive: true });

if (missing.length > 0) {
  writeFileSync(
    outputPath,
    [
      '// Generated by scripts/write-firebase-messaging-sw-config.mjs',
      '// Firebase Messaging is disabled because required env vars are missing.',
      'self.KANDILO_FIREBASE_MESSAGING_CONFIG = null;',
      '',
    ].join('\n')
  );
  console.warn(`Firebase Messaging service worker config disabled. Missing: ${missing.join(', ')}`);
  process.exit(0);
}

writeFileSync(
  outputPath,
  [
    '// Generated by scripts/write-firebase-messaging-sw-config.mjs',
    '// This file is intentionally gitignored.',
    `self.KANDILO_FIREBASE_MESSAGING_CONFIG = ${JSON.stringify(firebaseConfig, null, 2)};`,
    '',
  ].join('\n')
);
console.log(`Wrote ${outputPath}`);
